#!/usr/bin/env node

import { spawn } from 'node:child_process';
import { createServer } from 'node:http';
import { existsSync } from 'node:fs';
import { mkdir, readFile, rm, writeFile } from 'node:fs/promises';
import { tmpdir } from 'node:os';
import { dirname, extname, relative, resolve, sep } from 'node:path';
import yaml from 'js-yaml';
import { createImplementationReportTemplate } from './implementation-report.lib.mjs';

const args = process.argv.slice(2);
const workspace = resolve(valueAfter(args, '--workspace') ?? process.cwd());
const blueprintPath = valueAfter(args, '--blueprint');
const url = valueAfter(args, '--url');
const outputArg = valueAfter(args, '--output');

if (!blueprintPath || !url) {
  console.error('Usage: node scripts/capture-implementation-report.mjs --workspace <app> --blueprint <screen.ui.json|yaml> --url <preview-url> [--output <report.json>]');
  process.exit(2);
}

const blueprint = await readBlueprint(resolveInside(workspace, blueprintPath));
const report = createImplementationReportTemplate(blueprint);
const reportDir = resolve(workspace, '.aub', 'reports');
const assetDir = resolve(reportDir, 'assets');
await mkdir(assetDir, { recursive: true });

const chromePort = await reservePort();
const chromeDir = resolve(tmpdir(), `aub-report-chrome-${process.pid}`);
const chrome = spawn(findChrome(), [
  '--headless=new',
  '--disable-gpu',
  '--hide-scrollbars',
  '--no-first-run',
  '--no-default-browser-check',
  `--remote-debugging-port=${chromePort}`,
  `--user-data-dir=${chromeDir}`,
  'about:blank',
], { stdio: ['ignore', 'ignore', 'pipe'] });

let chromeError = '';
let chromeSpawnError = null;
chrome.on('error', (error) => {
  chromeSpawnError = error;
});
chrome.stderr.setEncoding('utf8');
chrome.stderr.on('data', (chunk) => { chromeError += chunk; });

try {
  const target = await openChromeTarget(chromePort, () => chromeSpawnError);
  const cdp = await connectCdp(target.webSocketDebuggerUrl);
  try {
    await cdp.command('Page.enable');
    await cdp.command('Runtime.enable');
    const captures = [];
    for (const viewport of blueprint.viewports) {
      captures.push(await captureViewport({ cdp, viewport, url, blueprint, assetDir, workspace }));
    }
    const sourceFiles = [...new Set(blueprint.nodes.map((node) => node.source?.file).filter(Boolean))];
    report.implementation = {
      framework: blueprint.provenance?.framework ?? 'web',
      route: new URL(url).pathname,
      files: sourceFiles,
    };
    report.node_mappings = blueprint.nodes.map((node) => ({
      node_id: node.id,
      status: 'mapped',
      component: node.type,
      file: node.source?.file ?? sourceFiles[0] ?? blueprintPath,
      selector: `[data-aub-node="${node.id}"]`,
      notes: 'Captured from preview route. If the selector is absent, source reference is used as the mapping fallback.',
    }));
    const firstCapture = captures[0];
    report.acceptance_results = blueprint.acceptance.map((item) => ({
      acceptance_id: item.id,
      status: captures.some((capture) => capture.horizontalOverflow) ? 'fail' : 'pass',
      evidence: [
        ...captures.map((capture) => ({
          type: 'screenshot',
          reference: capture.screenshot,
          viewport: capture.viewport,
          bytes: capture.screenshotBytes,
        })),
        ...captures.map((capture) => ({
          type: 'overflow',
          reference: `${capture.viewport}:horizontal-overflow`,
          viewport: capture.viewport,
          expected: false,
          actual: capture.horizontalOverflow,
          pass: !capture.horizontalOverflow,
        })),
        {
          type: 'dom_query',
          reference: '[data-aub-node="root"]',
          selector: '[data-aub-node="root"]',
          expected: true,
          actual: Boolean(firstCapture?.rootNodeFound),
        },
      ],
      notes: 'Generated by AUB preview capture. Review component-specific assertions before merging.',
    }));
  } finally {
    cdp.close();
  }
} catch (error) {
  throw new Error(`AUB report capture failed: ${error.message}\n${chromeError.slice(-2000)}`);
} finally {
  if (!chrome.killed) chrome.kill('SIGTERM');
  await rm(chromeDir, { recursive: true, force: true });
}

const outputPath = outputArg
  ? resolveInside(workspace, outputArg)
  : resolve(workspace, '.aub', 'reports', `${blueprint.screen.id}.implementation-report.json`);
await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, `${JSON.stringify(report, null, 2)}\n`, 'utf8');
console.log(JSON.stringify({
  report: relativePath(workspace, outputPath),
  screenshots: report.acceptance_results[0]?.evidence.filter((item) => item.type === 'screenshot').map((item) => item.reference) ?? [],
}, null, 2));

async function captureViewport({ cdp, viewport, url, blueprint, assetDir, workspace }) {
  await cdp.command('Emulation.setDeviceMetricsOverride', {
    width: viewport.width,
    height: viewport.height,
    deviceScaleFactor: 1,
    mobile: viewport.id === 'mobile',
  });
  await cdp.command('Page.navigate', { url });
  await waitForDocument(cdp);
  const measurement = await evaluate(cdp, `(() => {
    const root = document.querySelector('[data-aub-node="root"]');
    return {
      rootNodeFound: Boolean(root),
      horizontalOverflow: document.documentElement.scrollWidth > window.innerWidth + 1,
      nodeCount: ${JSON.stringify(blueprint.nodes.map((node) => node.id))}.filter((id) => document.querySelector('[data-aub-node="' + id + '"]')).length
    };
  })()`);
  const screenshot = await cdp.command('Page.captureScreenshot', {
    format: 'png',
    fromSurface: true,
    captureBeyondViewport: false,
  });
  const screenshotBytes = Buffer.from(screenshot.data, 'base64');
  const screenshotName = `${blueprint.screen.id}-${viewport.id}.png`.replace(/[^a-zA-Z0-9._-]+/g, '-');
  const screenshotPath = resolve(assetDir, screenshotName);
  await writeFile(screenshotPath, screenshotBytes);
  return {
    viewport: viewport.id,
    screenshot: relativePath(workspace, screenshotPath),
    screenshotBytes: screenshotBytes.length,
    rootNodeFound: Boolean(measurement.rootNodeFound),
    horizontalOverflow: Boolean(measurement.horizontalOverflow),
    nodeCount: Number(measurement.nodeCount) || 0,
  };
}

async function readBlueprint(path) {
  const text = await readFile(path, 'utf8');
  const extension = extname(path).toLowerCase();
  return extension === '.yaml' || extension === '.yml' ? yaml.load(text) : JSON.parse(text);
}

function valueAfter(argv, flag) {
  const index = argv.indexOf(flag);
  return index >= 0 ? argv[index + 1] : undefined;
}

function resolveInside(root, path) {
  const absolute = resolve(root, path);
  const rel = relative(root, absolute);
  if (rel === '..' || rel.startsWith(`..${sep}`) || rel.startsWith('/')) {
    throw new Error(`Path must stay inside the workspace root: ${path}`);
  }
  return absolute;
}

function relativePath(root, path) {
  return relative(root, path).split(sep).join('/');
}

async function waitForDocument(cdp) {
  for (let attempt = 0; attempt < 80; attempt += 1) {
    const ready = await evaluate(cdp, 'document.readyState === "complete"');
    if (ready) {
      await new Promise((resolveWait) => setTimeout(resolveWait, 50));
      return;
    }
    await new Promise((resolveWait) => setTimeout(resolveWait, 25));
  }
  throw new Error('Timed out waiting for preview document.');
}

async function evaluate(cdp, expression) {
  const response = await cdp.command('Runtime.evaluate', {
    expression,
    returnByValue: true,
    awaitPromise: true,
  });
  if (response.exceptionDetails) throw new Error(response.exceptionDetails.text ?? 'Runtime evaluation failed.');
  return response.result?.value;
}

async function openChromeTarget(port, getSpawnError) {
  for (let attempt = 0; attempt < 100; attempt += 1) {
    const spawnError = getSpawnError?.();
    if (spawnError) throw spawnError;
    try {
      const response = await fetch(`http://127.0.0.1:${port}/json/new?about:blank`, { method: 'PUT' });
      if (response.ok) return response.json();
    } catch {
      // Chrome is still starting.
    }
    await new Promise((resolveWait) => setTimeout(resolveWait, 50));
  }
  throw new Error('Timed out connecting to local Chrome.');
}

async function connectCdp(url) {
  if (typeof WebSocket === 'undefined') {
    throw new Error('This command requires a Node.js runtime with global WebSocket support.');
  }
  const socket = new WebSocket(url);
  await new Promise((resolveOpen, rejectOpen) => {
    socket.addEventListener('open', resolveOpen, { once: true });
    socket.addEventListener('error', rejectOpen, { once: true });
  });
  let nextId = 1;
  const pending = new Map();
  socket.addEventListener('message', (event) => {
    const message = JSON.parse(String(event.data));
    if (!message.id) return;
    const request = pending.get(message.id);
    if (!request) return;
    pending.delete(message.id);
    if (message.error) request.reject(new Error(message.error.message));
    else request.resolve(message.result ?? {});
  });
  return {
    command(method, params = {}) {
      const id = nextId;
      nextId += 1;
      return new Promise((resolveCommand, rejectCommand) => {
        pending.set(id, { resolve: resolveCommand, reject: rejectCommand });
        socket.send(JSON.stringify({ id, method, params }));
      });
    },
    close() {
      socket.close();
    },
  };
}

function reservePort() {
  const server = createServer();
  return new Promise((resolvePort, rejectPort) => {
    server.once('error', rejectPort);
    server.listen(0, '127.0.0.1', () => {
      const address = server.address();
      const port = typeof address === 'object' && address ? address.port : 0;
      server.close(() => resolvePort(port));
    });
  });
}

function findChrome() {
  const candidates = [
    process.env.AUB_CHROME_BIN,
    '/Applications/Google Chrome.app/Contents/MacOS/Google Chrome',
    '/Applications/Chromium.app/Contents/MacOS/Chromium',
    '/Applications/Microsoft Edge.app/Contents/MacOS/Microsoft Edge',
    'google-chrome',
    'chromium',
  ].filter(Boolean);
  const absolute = candidates.find((candidate) => candidate.startsWith('/') && existsSync(candidate));
  return absolute ?? candidates.find((candidate) => !candidate.startsWith('/')) ?? candidates[0];
}
